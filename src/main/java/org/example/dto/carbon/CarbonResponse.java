package org.example.dto.carbon;

/**
 * Результат расчёта усиления композиционными материалами (Раздел 13).
 * Заполняются только поля, относящиеся к выбранному режиму; остальные приходят нулевыми.
 * Моменты — в кН·м, силы — в кН, высоты сжатой зоны — в метрах, допускаемые нагрузки k — в кН/м.
 */
public record CarbonResponse(

        String mode,                // эхо выбранного режима
        String reinfType,           // тип конструкции усиления (табл. 13.4)
        String reinfTypeTitle,      // его расшифровка
        Double ks,                  // коэффициент k_s из табл. 13.4 (ф. 13.2)

        // === Прочность по моменту (13.2–13.9) ===
        Double sigmaFu,             // σ_fu — предельное напряжение в материале усиления (ф. 13.2), МПа
        Double sigmaFu2,            // σ_fu,2 — напряжение в боковом материале (ф. 13.3), МПа
        Double omega,               // ω = 0,85 − 0,008·R_b (ф. 7.13)
        Double xiFy,                // ξ_fy — граничная относительная высота сжатой зоны (ф. 13.4)
        Double x,                   // x — высота сжатой зоны бетона, м
        Boolean boundaryInWeb,      // true — граница сжатой зоны в ребре (13.8/13.9)
        Boolean xiCapped,           // true — сработало ограничение ξ > ξ_fy (ф. 13.5)
        Double my,                  // M^y — предельный момент усиленного сечения (ф. 13.7/13.9), кН·м
        Double kMoment,             // допускаемая нагрузка по моменту (ф. 13.1), кН/м

        // === Прочность по поперечной силе (13.10–13.16) ===
        Double c,                   // длина проекции наклонного сечения (ф. 13.14), м
        Double qb,                  // Q_b — воспринимаемая бетоном (ф. 13.13), кН
        Double qUnstrengthened,     // Q — предельная поперечная сила неусиленного сечения (ф. 13.12), кН
        Double qyConcrete,          // Q^y по сжатому бетону между трещинами (ф. 13.15), кН
        Double qyCrack,             // Q^y по наклонной трещине (ф. 13.16), кН
        Double qy,                  // Q^y = min(13.15, 13.16), кН
        Double qpCalc,              // Q_p от постоянных нагрузок (ф. 13.11), кН
        Double kShear,              // допускаемая нагрузка по поперечной силе (ф. 13.10), кН/м

        // === Момент в наклонном сечении (13.17–13.18) ===
        Double myInclined,          // M^y наклонного сечения (ф. 13.18), кН·м
        Double kInclined,           // допускаемая нагрузка (ф. 13.17), кН/м

        // === Выносливость (13.19–13.28) ===
        Double rbfUsed,             // R_bf, принятое в расчёте, МПа
        Double rsfUsed,             // R_sf, принятое в расчёте, МПа
        Double xyFatigue,           // x'_y — высота сжатой зоны для выносливости (ф. 13.23), м
        Boolean fatigueTee,         // true — тавровое сечение (13.22), false — прямоугольное (13.26)
        Double iRedFatigue,         // I^y_red — приведённый момент инерции (ф. 13.22/13.26), м⁴
        Double myConcrete,          // M^y по выносливости бетона (ф. 13.20), кН·м
        Double myRebar,             // M^y по выносливости арматуры (ф. 13.21), кН·м
        Double es,                  // коэффициент e_s (табл. 13.5)
        Double kFatigueConcrete,    // допускаемая нагрузка по выносливости бетона (ф. 13.19), кН/м
        Double kFatigueRebar,       // допускаемая нагрузка по выносливости арматуры (ф. 13.19), кН/м

        // === Технология усиления (13.29) ===
        Double mub,                 // M^уб — предельный момент с учётом технологии (ф. 13.29), кН·м

        // === Итог ===
        Double governingK,          // определяющая допускаемая нагрузка для режима, кН/м
        String note,                // краткое пояснение

        String detailedReport
) {}
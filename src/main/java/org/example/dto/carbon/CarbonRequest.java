package org.example.dto.carbon;

import jakarta.validation.Valid;
import jakarta.validation.constraints.NotNull;
import org.example.dto.common.BridgeCommonData;
import org.example.dto.slab.MaterialsData;

/**
 * Запрос на расчёт усиления пролётного строения композиционными материалами
 * на основе углеродного волокна (Раздел 13).
 *
 * <p>Раздел объединяет пять разных проверок усиленной балки; какая выполняется, задаёт
 * поле {@code mode}. Заполнять нужно только относящиеся к выбранному режиму поля.
 *
 * <p>Допустимые значения {@code mode}:
 * <ul>
 *   <li>{@code STRENGTH_MOMENT} — 13.2, прочность нормального сечения по моменту (ф. 13.1–13.9);</li>
 *   <li>{@code STRENGTH_SHEAR} — 13.2, прочность наклонного сечения по поперечной силе (ф. 13.10–13.16);</li>
 *   <li>{@code INCLINED_MOMENT} — 13.2, момент в наклонном сечении (ф. 13.17–13.18);</li>
 *   <li>{@code FATIGUE} — 13.3, выносливость усиленного сечения (ф. 13.19–13.28);</li>
 *   <li>{@code TECHNOLOGY} — 13.4, учёт технологии усиления (ф. 13.29).</li>
 * </ul>
 *
 * <p>Допустимые значения {@code reinfType} (таблица 13.4, коэффициент k_s в ф. 13.2):
 * {@code SHEET_BOTTOM_FREE}, {@code SHEET_BOTTOM_ANCHORED}, {@code U_JACKET_FREE},
 * {@code U_JACKET_ANCHORED}, {@code PLATE_ANCHORED}.
 *
 * <p>ЕДИНИЦЫ: сопротивления — МПа, линейные размеры — м, площади — м².
 * Толщина слоя t_f и единичная ширина полосы b_f — в миллиметрах (так они входят в ф. 13.2).
 */
public record CarbonRequest(

        @NotNull
        String mode,

        @NotNull @Valid
        BridgeCommonData commonData,

        // === Материалы (Раздел 5) ===
        MaterialsData materials,
        Double rsc,                 // R_sc, МПа (для ненапрягаемой арматуры = R_s)
        Double rsw,                 // R_sw — сопротивление хомутов, МПа (ф. 13.18)
        Double rhoB,                // асимметрия цикла напряжений в бетоне (для ф. 5.1)
        Double rhoS,                // асимметрия цикла напряжений в арматуре (для ф. 5.2)
        Double rbf,                 // R_bf, МПа — если задано, отменяет расчёт по ф. 5.1
        Double rsf,                 // R_sf, МПа — если задано, отменяет расчёт по ф. 5.2

        // === Материал усиления (таблицы 13.1–13.4) ===
        Double ef,                  // E_f — модуль упругости материала усиления, МПа
        Double rf,                  // R_f — расчётное сопротивление на растяжение, МПа
        Double tfLayerMm,           // t_f — толщина одного слоя, мм
        Integer nLayers,            // количество слоёв
        Double bUnitMm,             // b_f — единичная ширина полосы, мм (ф. 13.2)
        Double epsBult,             // ε_b,ult — предельная деформация бетона (ф. 13.4)
        String reinfType,           // тип конструкции усиления (табл. 13.4)

        // === Геометрия сечения ===
        Double b,                   // ширина ребра балки, м
        Double bfPrime,             // расчётная ширина сжатого пояса b'_f, м
        Double hfPrime,             // приведённая толщина сжатого пояса h'_f, м
        Double h,                   // полная высота главной балки, м
        Double h0,                  // рабочая высота сечения, м
        Double asPrimeDist,         // a'_s — расстояние от сжатой грани до центра сжатой арматуры, м
        Double asDist,              // a_s — расстояние от растянутой грани до центра растянутой арматуры, м
        Double au,                  // a_u — до ц.т. ближайшего ряда арматуры, м (FATIGUE)
        Double dJacket,             // d — высота полок U-образной обоймы, м (ф. 13.3)

        // === Арматура, м² ===
        Double asArea,              // A_s — площадь растянутой арматуры
        Double asPrimeArea,         // A'_s — площадь сжатой арматуры
        Double asw,                 // площадь одной ветви хомутов
        Double sStirrup,            // шаг хомутов, м
        Double sumAsiSinAlpha,      // Σ A_si·sinα по отогнутым стержням, м²
        Double sumAsi,              // Σ A_si по отогнутым стержням, м² (ф. 13.18)
        Double cShear,              // c — длина проекции наклонного сечения, м (0 — по ф. 13.14)

        // === Материал усиления: площади, м² ===
        Double af1,                 // A_f1 — на нижней грани балки
        Double af2,                 // A_f2 — на боковых гранях балки
        Double afw,                 // Σ площадей вертикальных холстов
        Double afi,                 // Σ площадей наклонных холстов
        Double phiFiberDeg,         // φ — угол наклона холстов, град (ф. 13.16)
        Double afPrimeArea,         // A'_f — в формулах момента инерции (FATIGUE)

        // === Плечи z для момента в наклонном сечении (ф. 13.18), м ===
        Double zS, Double zSw, Double zSi,
        Double zC1, Double zC2, Double zCw, Double zCi,

        // === Нагрузки и коэффициенты ===
        Double mUlt,                // M — предельный момент неусиленного сечения, кН·м
        Double mp,                  // M_p — момент от постоянных нагрузок, кН·м
        Double qUlt,                // Q — предельная поперечная сила неусиленного сечения, кН (0 — по 13.12)
        Double qp,                  // Q_p — поперечная сила от постоянных нагрузок, кН (0 — по 13.11)
        Double nk,                  // коэффициент надёжности к временной нагрузке
        Double np,                  // коэффициент надёжности к весу ж/б
        Double npPrime,             // коэффициент надёжности к весу балласта
        Double pp,                  // постоянная нагрузка от веса ПС на балку, кН/м
        Double pb,                  // постоянная нагрузка от веса балласта на балку, кН/м
        Double epsM,                // ε_M — доля временной нагрузки на балку по моменту
        Double epsQ,                // ε_Q — доля временной нагрузки на балку по поперечной силе
        Double omegaM,              // Ω — площадь линии влияния момента, м (0 — принять l²/8)
        Double omegaK,              // Ω_к — площадь линии влияния поперечной силы, м
        Double omegaP,              // Ω_p — площадь линии влияния для Q_p (ф. 13.11), м
        Double theta,               // Θ — коэффициент для выносливости (ф. 13.19)

        // === Выносливость (ф. 13.20–13.28) ===
        Double nPrimeSteel,         // n' — условное отношение модулей сталь/бетон
        Double nPrimeFiber,         // n'_f — условное отношение модулей углеволокно/бетон (0 — E_f/E_b)

        // === Технология усиления (ф. 13.29) ===
        Double my,                  // M^y — предельный момент усиленного сечения, кН·м
        Double mk,                  // M_k — момент от временной нагрузки при усилении, кН·м
        Double myK                  // M^y_k, кН·м
) {}
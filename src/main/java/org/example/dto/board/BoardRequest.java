package org.example.dto.board;

import jakarta.validation.constraints.NotNull;
import jakarta.validation.constraints.Positive;
import jakarta.validation.constraints.PositiveOrZero;
import org.example.dto.slab.MaterialsData;

/**
 * Запрос на расчёт грузоподъёмности продольного борта балластного корыта (Раздел 10).
 *
 * <p>Материалы приходят из результата Раздела 5, коэффициенты надёжности и удельный вес
 * балласта — из Раздела 6. Остальное вводится пользователем на странице раздела.
 */
public record BoardRequest(

        // === Материалы (Раздел 5) ===
        MaterialsData materials,
        Double rsc,                 // сопротивление арматуры сжатию, МПа (для ненапрягаемой = Rs)

        // === Коэффициенты и нагрузки из Раздела 6 ===
        Double np,                  // коэф. надёжности к весу ж/б (по умолчанию 1.1)
        Double npPrime,             // коэф. надёжности к весу балласта (по умолчанию 1.2)
        Double nk,                  // коэф. надёжности к временной нагрузке (по умолчанию 1.15)
        Double gammaBallast,        // удельный вес балласта с частями пути, кН/м³ (по умолчанию 19.6)
        Double dynamicCoeff,        // динамический коэффициент (1+μ) для расчёта класса

        // === Геометрия борта ===
        @NotNull @Positive
        Double hb,                  // толщина слоя балласта под шпалой (глубина z), м
        @NotNull @PositiveOrZero
        Double xShoulder,           // плечо балластной призмы x (аргумент табл. 10.2 / 10.4), м
        Double hbr,                 // высота продольного борта, м
        Double ls,                  // длина шпалы, м
        Double b,                   // расчётная ширина борта, м (по Руководству 1 м)
        Double aBallastCm,          // расстояние от торца шпалы до борта, см (для Δ_br)

        // === Сечение борта и арматура ===
        @NotNull @Positive
        Double h0,                  // рабочая высота сечения, м
        @NotNull @PositiveOrZero
        Double asArea,              // площадь растянутой арматуры As, м²
        Double asPrimeArea,         // площадь сжатой арматуры As', м²
        Double asPrimeDist,         // расстояние от сжатой грани до центра сжатой арматуры a', м

        // === Параметры для поперечной силы (ф. 7.17) ===
        Double cShear,              // длина проекции наклонного сечения, м (0 — принять h0)
        Double sStirrup,            // шаг хомутов, м
        Double asw,                 // площадь одной ветви хомутов, м²
        Double sumAsiSinAlpha,      // сумма A_si·sinα отогнутых стержней, м²

        // === Постоянные нагрузки на борт ===
        Double phiFrictionDeg,      // угол внутреннего трения балласта, град (ф. 10.8)
        Double pt,                  // нагрузка от веса тротуара и перил, кН/м (ф. 10.7)
        Double yt,                  // расстояние от ц.т. тротуара до внутренней грани борта, м
        Double p0,                  // сосредоточенная постоянная нагрузка, кН (ф. 10.10)
        Double lt,                  // геометрический параметр для Q_p, м (ф. 10.10)
        Double lk,                  // геометрический параметр для Q_p, м (ф. 10.10)

        // === Кривая ===
        Double curveRadius,         // радиус кривой R, м (0 — прямой участок)
        Integer track,              // 1 - прямая, 2 - кривая R=600, 3 - кривая R=300 (табл. 10.3)

        // === Выносливость (п. 10.3) ===
        Double rhoB,                // асимметрия цикла напряжений для бетона (п. 5.2.2)
        Double rhoS,                // асимметрия цикла напряжений для арматуры (п. 5.2.2)
        Double epsilonDyn,          // коэф. уменьшения динамического воздействия ε (Приложение 3)

        // === Приведение к классу (ф. 4.1) ===
        Double kc                   // эталонная нагрузка, кН/м; 0 или null — класс не считать
) {}
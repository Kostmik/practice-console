package org.example.dto.board;

/**
 * Результат расчёта грузоподъёмности продольного борта (Раздел 10).
 * Допускаемые нагрузки k — в кН/м, моменты — в кН·м, силы — в кН.
 */
public record BoardResponse(

        // === Промежуточные величины ===
        Double sigmaBmax,           // σ_b,max — горизонтальное давление балласта, кН/м² (ф. 10.8)
        Double mp,                  // M_p — момент от постоянных нагрузок, кН·м (ф. 10.7)
        Double qp,                  // Q_p — поперечная сила от постоянных нагрузок, кН (ф. 10.10)
        Double mLimit,              // предельный изгибающий момент M, кН·м (ф. 7.11/7.14)
        Double qLimit,              // предельная поперечная сила Q, кН (ф. 7.17)

        // === Табличные функции ===
        Double fCoeff,              // F(a,z) по табл. 10.2, 1/м²
        Double phiCoeff,            // Ф(a,z) по табл. 10.4, 1/м

        // === Выносливость ===
        Double rbf,                 // сопротивление бетона на выносливость, МПа (ф. 5.1)
        Double rsf,                 // сопротивление арматуры на выносливость, МПа (ф. 5.2)
        Double xFatigue,            // высота сжатой зоны x', м (ф. 7.39)
        Double iRed,                // момент инерции приведённого сечения, м⁴ (ф. 7.38)

        // === Допускаемые временные нагрузки ===
        Double kMoment,             // по прочности на изгиб (ф. 10.1), кН/м
        Double kShear,              // по прочности на поперечную силу (ф. 10.9), кН/м
        Double kFatigueConcrete,    // по выносливости бетона (ф. 10.11), кН/м
        Double kFatigueRebar,       // по выносливости арматуры (ф. 10.12), кН/м

        // === Итог ===
        Double kGoverning,          // определяющая (минимальная) допускаемая нагрузка, кН/м
        String governingCase,       // какой расчёт оказался определяющим
        Double classK,              // класс борта K (ф. 4.1), null если не считался

        String detailedReport
) {}
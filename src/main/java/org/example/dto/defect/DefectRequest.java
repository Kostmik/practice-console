package org.example.dto.defect;

import jakarta.validation.constraints.NotNull;
import org.example.dto.slab.MaterialsData;

import java.util.List;

/**
 * Запрос на учёт влияния дефектов пролётного строения (Раздел 12).
 *
 * <p>Один запрос описывает ОДИН вид дефекта — он выбирается полем {@link #defectType}.
 * Заполнять нужно только относящиеся к нему поля, остальные можно не передавать.
 *
 * <p>Допустимые значения {@code defectType}:
 * <ul>
 *   <li>{@code CORROSION} — 12.2, ослабление арматуры коррозией и выключенные стержни (ф. 12.1);</li>
 *   <li>{@code CRACK_IN_COMPRESSION} — 12.3, трещины в сжатой зоне (ф. 12.2–12.3);</li>
 *   <li>{@code VOID_RECTANGULAR} — 12.4, раковины/сколы, прямоугольное сечение (ф. 12.4, 12.5);</li>
 *   <li>{@code VOID_TSECTION} — 12.4, раковины/сколы, тавровое сечение (ф. 12.6, 12.5);</li>
 *   <li>{@code VOID_PRESTRESSED} — 12.5, напрягаемая арматура, тавровое сечение (ф. 12.7, 12.5).</li>
 * </ul>
 */
public record DefectRequest(

        @NotNull
        String defectType,

        // === Материалы (Раздел 5) ===
        MaterialsData materials,
        Double rsc,                 // сопротивление сжатой арматуры, МПа (для ненапрягаемой = Rs)
        Double rp,                  // сопротивление напрягаемой арматуры R_p, МПа (для ф. 12.7)
        Double sigmaPc,             // напряжение в напрягаемой арматуре сжатой зоны σ'_pc, МПа

        // === Геометрия сечения ===
        Double b,                   // ширина ребра (тавровое) или ширина сечения, м
        Double bfPrime,             // расчётная ширина сжатого пояса b'_f, м (тавровое)
        Double hfPrime,             // приведённая толщина сжатого пояса h'_f, м (тавровое)
        Double h0,                  // рабочая высота сечения, м
        Double asPrime,             // расстояние от сжатой грани до центра сжатой арматуры a'_s, м

        // === Арматура, м² ===
        Double asArea,              // площадь растянутой ненапрягаемой арматуры A_s
        Double asPrimeArea,         // площадь сжатой ненапрягаемой арматуры A'_s
        Double apArea,              // площадь растянутой напрягаемой арматуры A_p (ф. 12.7)
        Double apPrimeArea,         // площадь напрягаемой арматуры сжатой зоны A'_p (ф. 12.7)
        Double apPrimeDist,         // расстояние от сжатой грани до центра A'_p, м (ф. 12.7)

        // === 12.2: относительное изменение площади арматуры j (ф. 12.1) ===
        Integer nBars,              // n — общее число стержней рабочей арматуры
        Double faOne,               // f_a — площадь сечения одного неповреждённого стержня
        List<Double> corrodedLoss,  // f_i — площади ослабления коррозией по каждому повреждённому стержню
        Integer nDisconnected,      // n_2 — число стержней, выключенных из работы

        // === 12.3: трещина в сжатой зоне ===
        Double xBarPhi,             // x̄_φ — высота сжатой зоны по эпюре раскрытия трещины, м
        Double mBar,                // M̄ — момент от испытательной нагрузки, кН·м
        Double mUlt,                // M — предельный момент по разделу 7, кН·м (≤ 0 — посчитать самим)

        // === 12.4–12.7: раковина/скол бетона в сжатой зоне ===
        Double a0Area,              // A_0 — площадь ослабления сечения, м²
        Double a0Dist               // a_0 — расстояние от растянутой арматуры до ц.т. ослабления, м
) {}
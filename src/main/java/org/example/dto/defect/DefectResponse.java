package org.example.dto.defect;

/**
 * Результат учёта влияния дефектов (Раздел 12).
 *
 * <p>Заполняются только те поля, которые относятся к выбранному виду дефекта:
 * при {@code CORROSION} — блок 12.2, при {@code CRACK_IN_COMPRESSION} — блок 12.3,
 * при остальных — блок раковин/сколов. Незаполненные поля возвращаются нулевыми.
 */
public record DefectResponse(

        String defectType,          // эхо выбранного вида дефекта

        // === 12.2 (ф. 12.1) ===
        Double asFull,              // A_s = n·f_a — площадь без учёта ослабления
        Double asActual,            // A_si — фактическая площадь с учётом коррозии и выключенных стержней
        Double j,                   // относительное изменение площади арматуры j = A_si / A_s

        // === 12.3 (ф. 12.2–12.3) ===
        Boolean condition122,       // выполнено ли условие 12.2: M̄ ≥ 0,8·M
        Double xPhi,                // стабильная высота сжатой зоны x_φ, м
        Double xDesign,             // расчётная высота сжатой зоны по разделу 7 (без трещины), м
        Boolean crackReducesCapacity, // true, если x_φ < x_design
        Double mCrack,              // предельный момент с учётом трещины, кН·м

        // === 12.4–12.7 (раковины/сколы) ===
        Double x0,                  // высота сжатой зоны с учётом ослабления (ф. 12.4 / 12.6 / 12.7), м
        Double mx0,                 // предельный момент при x_0 без вычета раковины, кН·м
        Double m0,                  // предельный момент с учётом ослабления M_0 (ф. 12.5), кН·м

        // === Итог ===
        Double governingMoment,     // момент, который подставляется в расчёт грузоподъёмности, кН·м
                                    // (для CORROSION не заполняется — там результатом является j)
        String note,                // краткое пояснение, что было вычислено

        String detailedReport
) {}
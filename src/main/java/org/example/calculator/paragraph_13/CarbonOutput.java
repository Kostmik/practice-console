package org.example.calculator.paragraph_13;

/**
 * Результаты расчёта усиления композиционными материалами (Раздел 13).
 * Заполняются только поля, относящиеся к выбранному {@link CarbonInput.Mode}.
 * Моменты — в кН·м, силы — в кН, высоты сжатой зоны — в метрах, k — в кН/м.
 */
public class CarbonOutput {

    // --- прочность по моменту (13.2–13.9) ---
    public double sigmaFu;   // предельное напряжение в материале усиления σ_fu (ф. 13.2), МПа
    public double sigmaFu2;  // напряжение в боковом материале σ_fu,2 (ф. 13.3), МПа
    public double omega;     // характеристика сжатой зоны ω = 0,85 − 0,008·R_b (ф. 7.13)
    public double xiFy;      // граничная относительная высота сжатой зоны ξ_fy (ф. 13.4)
    public double x;         // высота сжатой зоны бетона x (ф. 13.6 / 13.8, с ограничением 13.5), м
    public boolean boundaryInWeb; // true — граница сжатой зоны в ребре (13.8/13.9), false — в плите (13.6/13.7)
    public boolean xiCapped; // true — сработало ограничение ξ > ξ_fy (принято x = ξ_fy·h0, ф. 13.5)
    public double My;        // предельный момент усиленного сечения M^y (ф. 13.7 / 13.9), кН·м
    public double kMoment;   // допускаемая нагрузка по моменту (ф. 13.1), кН/м

    // --- прочность по поперечной силе (13.10–13.16) ---
    public double c;         // длина проекции наклонного сечения (ф. 13.14 или задано), м
    public double Qb;        // поперечная сила, воспринимаемая бетоном Q_b (ф. 13.13), кН
    public double Qunstr;    // предельная поперечная сила неусиленного сечения Q (ф. 13.12), кН
    public double QyConcrete;// Q^y по сжатому бетону между трещинами (ф. 13.15), кН
    public double QyCrack;   // Q^y по наклонной трещине (ф. 13.16), кН
    public double Qy;        // предельная поперечная сила усиленного сечения = min(13.15, 13.16), кН
    public double QpCalc;    // Q_p от постоянных нагрузок (ф. 13.11), кН
    public double kShear;    // допускаемая нагрузка по поперечной силе (ф. 13.10), кН/м

    // --- момент в наклонном сечении (13.17–13.18) ---
    public double MyInclined; // предельный момент усиленного наклонного сечения (ф. 13.18), кН·м
    public double kInclined;  // допускаемая нагрузка (ф. 13.17), кН/м

    // --- выносливость (13.19–13.28) ---
    public double xyFatigue;  // высота сжатой зоны для выносливости x'_y (ф. 13.23), м
    public boolean fatigueTee; // true — тавровое сечение (13.22/13.24/13.25), false — прямоугольное (13.26/13.27/13.28)
    public double IredFatigue; // приведённый момент инерции I^y_red (ф. 13.22 / 13.26), м⁴
    public double MyConcrete; // M^y по выносливости бетона (ф. 13.20), кН·м
    public double MyRebar;    // M^y по выносливости арматуры (ф. 13.21), кН·м
    public double es;         // коэффициент e_s (таблица 13.5)
    public double kFatigueConcrete; // допускаемая нагрузка по выносливости бетона (ф. 13.19), кН/м
    public double kFatigueRebar;    // допускаемая нагрузка по выносливости арматуры (ф. 13.19), кН/м

    // --- технология усиления (13.29) ---
    public double Mub;        // предельный момент усиленного сечения с учётом технологии M^уб (ф. 13.29), кН·м

    /** Итоговая (определяющая) допускаемая временная нагрузка для выбранного режима, кН/м. */
    public double governingK;
    /** Краткое пояснение к результату. */
    public String note = "";
}
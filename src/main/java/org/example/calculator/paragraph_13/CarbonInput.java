package org.example.calculator.paragraph_13;

/**
 * Исходные данные для расчёта усиления пролётного строения композиционными материалами
 * на основе углеродного волокна (Раздел 13 Руководства).
 *
 * <p>Раздел 13 объединяет несколько разных проверок усиленной балки; какая именно выполняется,
 * задаёт поле {@link #mode}. Заполнять нужно только поля, относящиеся к выбранному режиму —
 * калькулятор в отчёте явно указывает, что было использовано. Расчётные характеристики бетона
 * и арматуры принимают по разделу 5, повреждения материала усиления учитывают по п. 12.6.
 *
 * <p>ЕДИНИЦЫ: сопротивления и напряжения — в МПа (переводятся в кПа внутри калькулятора при
 * умножении на площади); линейные размеры — в метрах, площади сечений — в м². Толщина слоя
 * материала усиления t_f и единичная ширина полосы b_f — в мм (так входят в ф. 13.2). Тогда
 * моменты получаются в кН·м, поперечные силы — в кН.
 */
public class CarbonInput {

    /** Какая проверка усиленной балки выполняется. */
    public enum Mode {
        /** 13.2 — прочность нормального сечения по изгибающему моменту (ф. 13.1–13.9). */
        STRENGTH_MOMENT,
        /** 13.2 — прочность наклонного сечения по поперечной силе (ф. 13.10–13.16). */
        STRENGTH_SHEAR,
        /** 13.2 — прочность наклонного сечения по изгибающему моменту (ф. 13.17–13.18). */
        INCLINED_MOMENT,
        /** 13.3 — выносливость усиленного сечения (ф. 13.19–13.28). */
        FATIGUE,
        /** 13.4 — учёт технологии усиления (ф. 13.29). */
        TECHNOLOGY
    }

    public Mode mode = Mode.STRENGTH_MOMENT;

    // === Материалы (Раздел 5), МПа ===
    public double Rb;        // расчётное сопротивление бетона сжатию
    public double Rbt;       // расчётное сопротивление бетона растяжению
    public double Rs;        // расчётное сопротивление растянутой арматуры
    public double Rsc;       // расчётное сопротивление сжатой арматуры (для ненапрягаемой = Rs)
    public double Rsw;       // расчётное сопротивление поперечной арматуры (хомутов)
    public double Es = 206000; // модуль упругости арматуры
    public double Eb;        // модуль упругости бетона
    public double Rbf;       // сопротивление бетона на выносливость (ф. 5.1), для FATIGUE
    public double Rsf;       // сопротивление арматуры на выносливость (ф. 5.2), для FATIGUE

    // === Материал усиления (углеволокно), таблицы 13.1–13.3 ===
    public double Ef;        // модуль упругости материала усиления, МПа
    public double Rf;        // расчётное сопротивление материала усиления на растяжение, МПа
    public double tfLayerMm; // толщина одного слоя материала усиления, мм
    public int nLayers = 1;  // количество слоёв материала усиления
    public double bUnitMm = 1.0; // единичная ширина полосы материала усиления, мм (ф. 13.2)
    public double epsBult = 0.0033; // предельная деформация бетона ε_b,ult (ф. 13.4)
    public CarbonTables.ReinforcementType reinfType = CarbonTables.ReinforcementType.U_JACKET_FREE; // тип усиления (табл. 13.4)

    // === Геометрия сечения ===
    public double b;         // ширина ребра балки, м
    public double bfPrime;   // расчётная ширина сжатого пояса (полки) b'_f, м
    public double hfPrime;   // приведённая толщина сжатого пояса (полки) h'_f, м
    public double h;         // полная высота главной балки, м
    public double h0;        // рабочая высота сечения, м
    public double asPrime;   // расстояние от сжатой грани до центра сжатой арматуры a'_s, м
    public double as;        // расстояние от растянутой грани до центра растянутой арматуры a_s, м
    public double au;        // расстояние от растянутой грани до ц.т. ближайшего ряда арматуры a_u, м (FATIGUE)
    public double d;         // высота полок U-образной обоймы материала усиления, м (ф. 13.3)

    // === Арматура, м² ===
    public double As;        // площадь растянутой арматуры
    public double AsPrime;   // площадь сжатой арматуры A'_s
    public double Asw;       // площадь одной ветви хомутов
    public double s;         // шаг хомутов, м
    public double sumAsiSinAlpha; // Σ A_si·sinα по отогнутым стержням, м² (для поперечной силы, ф. 13.12/13.16)
    public double sumAsi;    // Σ A_si по отогнутым стержням, м² (без sinα; для момента в наклонном сечении, ф. 13.18)
    public double cShear;    // длина проекции наклонного сечения c, м (0 — вычислить по ф. 13.14)

    // === Материал усиления: площади, м² ===
    public double Af1;       // площадь материала усиления на нижней грани балки
    public double Af2;       // площадь материала усиления на боковых гранях балки
    public double Afw;       // Σ площадей вертикальных холстов (для поперечной силы), м²
    public double Afi;       // Σ площадей наклонных холстов (для поперечной силы), м²
    public double phiFiberDeg = 90; // угол наклона холстов усиления φ, град (ф. 13.16)

    // === Расчёт по изгибающему моменту в наклонном сечении (ф. 13.18) — плечи z, м ===
    public double zS;   public double zSw;  public double zSi;
    public double zC1;  public double zC2;  public double zCw;  public double zCi;

    // === Нагрузки и коэффициенты ===
    public double M;         // предельный момент неусиленного сечения, кН·м
    public double Mp;        // момент от постоянных нагрузок (неусиленное сечение), кН·м
    public double Q;         // предельная поперечная сила неусиленного сечения, кН (0 — вычислить)
    public double Qp;        // поперечная сила от постоянных нагрузок, кН (0 — вычислить по 13.11)
    public double nk = 1.15; // коэффициент надёжности к временной нагрузке (п. 6.5)
    public double np = 1.1;  // коэффициент надёжности к весу ж/б (п. 6.3)
    public double npPrime = 1.2; // коэффициент надёжности к весу балласта (п. 6.3)
    public double pp;        // постоянная нагрузка от веса ПС на балку, кН/м (для ф. 13.11)
    public double pb;        // постоянная нагрузка от веса балласта на балку, кН/м (для ф. 13.11)
    public double epsM;      // доля временной нагрузки на балку по моменту ε_M
    public double epsQ;      // доля временной нагрузки на балку по поперечной силе ε_Q
    public double Omega;     // площадь линии влияния момента Ω, м
    public double OmegaK;    // площадь линии влияния поперечной силы Ω_к, м
    public double OmegaP;    // площадь линии влияния для Q_p (ф. 13.11), м
    public double Theta = 1.0; // коэффициент Θ (для выносливости, ф. 13.19)

    // === Выносливость (ф. 13.20–13.28) ===
    public double nPrimeSteel;  // условное отношение модулей упругости n' (сталь/бетон)
    public double nPrimeFiber;  // условное отношение модулей упругости n'_f (углеволокно/бетон)
    public double AfPrime;      // площадь материала усиления A'_f (в ф. момента инерции)

    // === Технология усиления (ф. 13.29) ===
    public double My;        // предельный момент усиленного сечения M^y (если уже посчитан отдельно), кН·м
    public double Mk;        // момент от временной нагрузки при усилении, кН·м
    public double MyK;       // M^y_k (в ф. 13.29), кН·м
}
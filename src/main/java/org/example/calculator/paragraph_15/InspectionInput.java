    public double b0Prime;     // b'₀ — ширина колеи по внутр. граням головок рельсов, м

    // ===== 15.4. Прочность бетона (ф. 15.2) =====
    public double[] concreteStrengths; // Rᵢ — единичные значения прочности бетона, МПа


package org.example.calculator.paragraph_11;

import org.example.util.Interpolation;

/**
 * Таблица 11.1 Руководства — коэффициент γ в зависимости от параметра g = 0,5·m·l.
 * Для промежуточных значений g — линейная интерполяция; за границами таблицы
 * берётся крайнее значение.
 */
public final class CurvedSpanTables {

    private CurvedSpanTables() {
    }

    // Параметр g = 0,5·m·l
    private static final double[] G = {
            0.10, 0.20, 0.30, 0.40, 0.50, 0.60, 0.70, 0.80, 0.90, 1.00,
            1.10, 1.20, 1.30, 1.40, 1.50, 1.60, 1.70, 1.80, 1.90, 2.00,
            2.10, 2.20, 2.30, 2.40, 2.50, 2.60, 2.70, 2.80, 2.90, 3.00,
            3.10, 3.20, 3.30, 3.40, 3.50, 3.60, 3.70, 3.80, 3.90, 4.00,
            4.10, 4.20, 4.30, 4.40, 4.50, 4.60, 4.70, 4.80, 4.90, 5.00, 5.10
    };

    // Коэффициент γ
    private static final double[] GAMMA = {
            1.01, 1.02, 1.05, 1.08, 1.13, 1.19, 1.26, 1.34, 1.43, 1.54,
            1.67, 1.81, 1.97, 2.15, 2.35, 2.58, 2.83, 3.11, 3.42, 3.76,
            4.14, 4.57, 5.04, 5.56, 6.13, 6.77, 7.47, 8.25, 9.11, 10.07,
            11.12, 12.29, 13.57, 15.00, 16.57, 18.31, 20.24, 22.36, 24.71, 27.31,
            30.18, 33.35, 36.85, 40.73, 45.01, 49.75, 54.98, 60.76, 67.15, 74.21, 82.01
    };

    /**
     * Коэффициент γ по параметру g (таблица 11.1, интерполяция).
     */
    public static double gamma(double g) {
        if (g <= G[0]) return GAMMA[0];
        if (g >= G[G.length - 1]) return GAMMA[GAMMA.length - 1];
        return Interpolation.linear(G, GAMMA, g);
    }
}
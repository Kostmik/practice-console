package org.example.calculator.paragraph_11;

/**
 * Исходные данные для расчёта доли временной нагрузки на главную балку пролётного строения,
 * расположенного на кривой (Раздел 11 Руководства).
 *
 * <p>Рассматривается монолитное пролётное строение с ненапрягаемой арматурой и двумя
 * главными балками под один путь. Результат расчёта — доли ε_M и ε_Q для балки 1 (наружной,
 * знак «+») и балки 2 (внутренней, знак «−»); эти доли далее используются в расчёте балки
 * так же, как для прямого участка (раздел 6/8).
 *
 * <p>Единицы: линейные размеры — в метрах, площади — в м², модули упругости — в МПа
 * (в формулах входят только отношением Es/Eb, поэтому единицы сокращаются),
 * скорость v — в км/ч, радиус R — в метрах.
 */
public class CurvedSpanInput {

    // === Геометрия поперечного сечения ===
    public double c;      // расстояние между осями балок, м
    public double b;      // ширина ребра (стенки) балки, м
    public double lk;     // геометрический параметр (для c1 = l_k + 0,5·b), м
    public double l;      // расчётный пролёт, м
    public double h;      // полная высота балки, м
    public double h1;     // средняя толщина плиты между рёбрами, м
    public double h2;     // средняя толщина плиты консоли, м
    public double hb;     // толщина слоя балласта, м
    public double hp;     // высота рельса, м
    public double hs;     // высота шпалы, м
    public double ht = 2.2; // высота приложения нагрузки, м (рисунок 11, обычно 2,2)
    public double as;     // расстояние до центра тяжести растянутой арматуры, м
    public double ls;     // длина шпалы, м

    // === Материалы ===
    public double Es;     // модуль упругости арматуры, МПа
    public double Eb;     // модуль упругости бетона, МПа
    public double As;     // площадь сечения растянутой арматуры, м²

    // === Кривая и путь ===
    public double R;      // радиус кривой, м
    public double v;      // наибольшая скорость движения поездов, км/ч
    public double cantElevation; // возвышение наружного рельса Δh, м
    public double b0;     // расстояние между осями головок рельсов, м
    public double lPrime;       // смещение оси пути l' (участок 0,25l…0,75l), м
    public double lDoublePrime; // смещение оси пути l'' (участки у опор), м
    public double Theta = 1.0;  // коэффициент Θ (приложение), безразмерный
    public double mu0 = 0.0;    // динамический коэффициент μ₀ (в формуле входит как 1+μ₀)
}
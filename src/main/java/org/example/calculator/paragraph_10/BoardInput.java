package org.example.calculator.paragraph_10;

/**
 * Исходные данные для расчёта грузоподъёмности продольного борта (Раздел 10).
 *
 * <p>Класс самодостаточен: материалы (R_b, R_s, ...) можно скопировать из результатов
 * Разделов 5–6 (например, из BridgeContext), но жёсткой зависимости от них нет.
 * Единицы указаны у каждого поля; сопротивления задаются в МПа и переводятся в кПа внутри
 * калькулятора.
 */
public class BoardInput {

    // === Геометрия борта ===
    public double Hbr;          // высота продольного борта, м
    public double ls;           // длина шпалы, м
    public double b = 1.0;      // расчётная ширина борта, м (по Руководству равна 1 м)
    public double hb;           // толщина слоя балласта под шпалой (глубина z), м
    public double xShoulder;    // плечо балластной призмы "x" (аргумент таблиц 10.2–10.4), м
    public double aBallastCm;   // расстояние от торца шпалы до борта, см (для Δ_br, необязательно)

    // === Сечение борта и арматура ===
    public double h0;           // рабочая высота сечения, м
    public double As;           // площадь растянутой арматуры, м²
    public double AsPrime;      // площадь сжатой арматуры, м²
    public double asPrime;      // расстояние от сжатой грани до центра сжатой арматуры, м

    // Параметры для поперечной силы (ф. 7.17). Для сплошного борта без хомутов/отгибов — нули.
    public double cShear = 0;         // длина проекции наклонного сечения, м
    public double sStirrup = 1.0;     // шаг хомутов, м
    public double Asw = 0;            // площадь одной ветви хомутов, м²
    public double sumAsiSinAlpha = 0; // сумма A_si·sinα отогнутых стержней, м²

    // === Материалы (из Раздела 5), МПа ===
    public double Rb;           // сопротивление бетона сжатию
    public double Rbt;          // сопротивление бетона растяжению
    public double Rs;           // сопротивление арматуры растяжению
    public double Rsc;          // сопротивление арматуры сжатию (для ненапрягаемой = Rs)
    public double nPrime;       // условное отношение модулей упругости n' (п. 5.2.1)

    // === Коэффициенты надёжности (Раздел 6) ===
    public double nk = 1.15;    // к временной нагрузке (п. 6.5)
    public double np = 1.1;     // к весу ж/б (п. 6.3)
    public double npPrime = 1.2; // к весу балласта (п. 6.3)

    // === Постоянные нагрузки на борт ===
    public double gammaBallast = 19.6; // объёмный вес балласта с частями пути, кН/м³ (ф. 10.8)
    public double phiFrictionDeg = 40; // угол внутреннего трения балласта, град (ф. 10.8)
    public double pt = 0;       // нагрузка от веса тротуара и перил, кН/м (ф. 10.7)
    public double yt = 0;       // расстояние от ц.т. тротуара до внутренней грани борта, м
    public double P0 = 0;       // сосредоточенная постоянная нагрузка, кН (ф. 10.10)
    public double lt = 0;       // геометрический параметр для Q_p, м (ф. 10.10)
    public double lk = 0;       // геометрический параметр для Q_p, м (ф. 10.10)

    // === Кривая (для τ, ф. 10.3). R = 0 или очень большой — считается как прямая. ===
    public double R = 0;        // радиус кривой, м
    public BoardTables.Track track = BoardTables.Track.STRAIGHT; // тип участка для табл. 10.3

    // === Выносливость (Раздел 10.3) ===
    public double rhoB = 0;     // асимметрия цикла напряжений для бетона (п. 5.2.2)
    public double rhoS = 0;     // асимметрия цикла напряжений для арматуры (п. 5.2.2)
    public double epsilonDyn = 1.0; // коэффициент уменьшения динамического воздействия (Приложение 3)

    // === Приведение к классу (необязательно, ф. 4.1) ===
    public double kc = 0;              // эталонная нагрузка (Приложение 1); 0 — класс не считать
    public double dynamicCoeff = 1.0;  // динамический коэффициент (1+μ)
}
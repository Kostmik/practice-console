package org.example.calculator.paragraph_12;

/**
 * Результаты учёта влияния дефектов (Раздел 12).
 * Заполняются только те поля, которые относятся к выбранному {@link DefectInput.DefectType}.
 * Моменты — в кН·м, высоты сжатой зоны — в метрах, коэффициент j — безразмерный.
 */
public class DefectOutput {

    // --- 12.2 (ф. 12.1) ---
    public double As_full;    // A_s = n·f_a — площадь без учёта ослабления
    public double As_actual;  // A_si — фактическая площадь с учётом коррозии и выключенных стержней
    public double j;          // относительное изменение площади арматуры j = A_si / A_s

    // --- 12.3 (ф. 12.2–12.3) ---
    public boolean condFormula82;  // выполнено ли условие 12.2: M̄ ≥ 0,8·M
    public double xPhi;            // стабильная высота сжатой зоны x_φ (равна x̄_φ либо по ф. 12.3), м
    public double xDesign;        // расчётная высота сжатой зоны по разделу 7 (без трещины), м
    public boolean crackReducesCapacity; // true, если x_φ < x_design (трещина снижает несущую способность)
    public double Mcrack;         // предельный момент с учётом трещины, кН·м

    // --- 12.4–12.7 (раковины/сколы) ---
    public double x0;             // высота сжатой зоны с учётом ослабления x_0 (ф. 12.4 / 12.6 / 12.7), м
    public double Mx0;            // предельный момент, вычисленный при x_0 без вычета раковины, кН·м
    public double M0;             // предельный момент с учётом ослабления M_0 = M − R_b·A_0·a_0 (ф. 12.5), кН·м

    /** Итоговый предельный изгибающий момент, который следует подставлять в расчёт грузоподъёмности. */
    public double governingMoment;
    /** Краткое описание того, что было вычислено. */
    public String note = "";
}
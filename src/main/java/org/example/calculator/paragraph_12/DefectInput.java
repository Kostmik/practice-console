package org.example.calculator.paragraph_12;

/**
 * Исходные данные для учёта влияния дефектов пролётного строения (Раздел 12 Руководства).
 *
 * <p>Раздел 12 не содержит самостоятельного «сквозного» расчёта грузоподъёмности, а
 * корректирует величины, входящие в расчёты на прочность разделов 7–9: фактическую площадь
 * арматуры (12.2), высоту сжатой зоны при трещинах (12.3) и предельный изгибающий момент при
 * раковинах и сколах бетона (12.4–12.7). Поэтому один экземпляр {@code DefectInput} описывает
 * один вид дефекта — вид выбирается полем {@link #type}, а заполнять нужно только относящиеся
 * к нему поля (в отчёте калькулятор явно указывает, что было использовано).
 *
 * <p>ЕДИНИЦЫ: сопротивления и напряжения — в МПа (переводятся в кПа внутри калькулятора),
 * линейные размеры — в метрах, площади — в м². Тогда моменты получаются в кН·м, а высота
 * сжатой зоны — в метрах. Площади отдельных стержней (f_a, f_i) в формуле 12.1 входят только
 * отношением, поэтому их можно задавать в любых согласованных единицах (см², мм², м²).
 */
public class DefectInput {

    /** Вид учитываемого дефекта (определяет, какие формулы раздела 12 применяются). */
    public enum DefectType {
        /** 12.2 — ослабление арматуры коррозией и выключенные из работы стержни (ф. 12.1). */
        CORROSION,
        /** 12.3 — трещины, заходящие в сжатую зону бетона (ф. 12.2–12.3). */
        CRACK_IN_COMPRESSION,
        /** 12.4 — раковины/сколы бетона, прямоугольное сечение (ф. 12.4–12.5). */
        VOID_RECTANGULAR,
        /** 12.4 — раковины/сколы бетона, тавровое сечение (ф. 12.6, 12.5). */
        VOID_TSECTION,
        /** 12.5 — дефекты в пролётных строениях с напрягаемой арматурой, тавровое сечение (ф. 12.7, 12.5). */
        VOID_PRESTRESSED
    }

    public DefectType type = DefectType.CORROSION;

    // === Материалы (Раздел 5), МПа ===
    public double Rb;        // расчётное сопротивление бетона сжатию
    public double Rs;        // расчётное сопротивление растянутой арматуры
    public double Rsc;       // расчётное сопротивление сжатой арматуры (для ненапрягаемой = Rs)
    public double Rp;        // расчётное сопротивление напрягаемой арматуры (для 12.7)
    public double sigmaPc;   // напряжение в напрягаемой арматуре сжатой зоны σ'_pc (для 12.7)

    // === Геометрия сечения ===
    public double b;         // расчётная ширина ребра (для таврового) или ширина сечения, м
    public double bfPrime;   // расчётная ширина сжатого пояса (полки) b'_f, м (тавровое)
    public double hfPrime;   // приведённая толщина сжатого пояса (полки) h'_f, м (тавровое)
    public double h0;        // рабочая высота сечения, м
    public double asPrime;   // расстояние от сжатой грани до центра сжатой арматуры a'_s, м

    // === Арматура, м² ===
    public double As;        // площадь растянутой ненапрягаемой арматуры (уже фактическая, с учётом 12.2)
    public double AsPrime;   // площадь сжатой ненапрягаемой арматуры A'_s
    public double Ap;        // площадь растянутой напрягаемой арматуры A_p (для 12.7)
    public double ApPrime;   // площадь напрягаемой арматуры сжатой зоны A'_p (для 12.7)
    public double apPrime;   // расстояние от сжатой грани до центра A'_p, м (для 12.7)

    // === 12.2: параметры для относительного изменения площади арматуры j (ф. 12.1) ===
    public int nBars;              // n — общее число стержней рабочей арматуры в элементе
    public double faOne;           // f_a — площадь сечения одного неповреждённого стержня
    public double[] corrodedLoss;  // f_i — площади ослабления сечения коррозией по каждому повреждённому стержню
    public int nDisconnected;      // n_2 — число стержней, выключенных из работы (потерявших сцепление/оборванных)

    // === 12.3: параметры для учёта трещины в сжатой зоне ===
    public double xBarPhi;   // x̄_φ — высота сжатой зоны по эпюре раскрытия трещины при испытании, м
    public double Mbar;      // M̄ — момент от испытательной нагрузки в рассчитываемом сечении, кН·м
    public double Mult;      // M — предельный изгибающий момент по разделу 7, кН·м
                             //     (если ≤ 0 — калькулятор вычислит его сам как для прямоугольного сечения)

    // === 12.4–12.7: параметры раковины/скола бетона в сжатой зоне ===
    public double A0;        // A_0 — площадь ослабления сечения раковиной/сколом в сжатой зоне, м²
    public double a0;        // a_0 — расстояние от растянутой арматуры до центра тяжести площади ослабления, м
}